import UIKit

var todo : [String] = ["Return Calls", "Write Blogpost", "Cook Dinner", "Pickup Laundry", "Buy Bulbs"]


var i = 0 // index
while i < todo.count { // condition
    println(todo[i])
    i++ // increment
}


for var j = 0; j < todo.count; j++ {
    println(todo[j])
}